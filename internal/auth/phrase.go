func NewPhrase() (string, error) {
	var words [4]string
	for i := range words {
		n, err := rand.Int(rand.Reader, big.NewInt(int64(len(effShort))))
		if err != nil {
			return "", fmt.Errorf("generate phrase: %w", err)
		}
		words[i] = effShort[n.Int64()]
	}
	return strings.Join(words[:], "-"), nil
}
